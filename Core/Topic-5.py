# Operators

# dealing with Int and float

print(2+2)

print(10/3)

# return only integer part
print(10//3)

# modulus return remainder of division.
print(10 % 3)

# exponent as power use double star
print(2**2)

# Augmented assignment operator
x = 10
x = x+10
print(x)

# use it as

y = 10
y += 10
print(y)

print(round(4.55))
print(abs(-4.55))